        sll $t3,$t3,2      # To obtain the offset in terms of bytes multiply by 4
        move $t4,$s1       #  copy the base address of "results" array from $s1 to $t4
        add $t4,$t4,$t3    #  Go to the location of the index of the selected letter in "results" array
        lw $t5,($t4)       #  Obtain the existing frequency of the selected letter
        addi $t5,$t5,1     # Increment it's frequency value by 1
        sw $t5,($t4)       # Store back the result back in "Results" array
        addi $t6,$t6,-1    # Decrease $t6
        addi $s0,$s0,4     # Go to the location of next charecter in the data
        bne $t6,$zero,traverse_array  # Continue, until $t6 becomes 0
        beq $t6,$zero,copy  # If $t6 becomes 0 jump to copy label

    upper:
        li $t2,65          # You're here because the letter was upper case
