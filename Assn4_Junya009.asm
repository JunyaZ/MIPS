#
# The Label Traverse Array basically, computes the fequency of each charecter in the data given.

traverse_array :
        lw $t1, 0($s0)  # take the first charecter.
        li $t2,97       # Comapare if the letter is lower or upper case if the ascii value is greater than 97 it's lower else upper.
        return: blt $t1,$t2,upper  # Jumps to it's upper counter_part
